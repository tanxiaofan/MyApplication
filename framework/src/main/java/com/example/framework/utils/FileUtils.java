    public static boolean isExistFile(String path) {
        try {
            File file = new File(path);
            if (file != null && file.exists()) {

                File[] files = file.listFiles(); // 读取
                if (files != null && files.length > 0) {
                    return true;
                } else {
                    return false;
                }
            }
        } catch (Exception e) {
            return false;
        }
        return false;
    }

    /**
     * delete file or directory
     * <p>
     * <ul>
     * <li>if path is null or empty, return true
     * <li>if path not exist, return true
     * <li>if path exist, delete recursion. return true
     * <ul>
     */
    public static boolean deleteFile(String path) {
        if (StringUtils.isBlank(path)) {
            return true;
        }

        File file = new File(path);
        if (!file.exists()) {
            return true;
        }
        if (file.isFile()) {
            return file.delete();
        }
        if (!file.isDirectory()) {
            return false;
        }
        if (file.listFiles() != null) {
            for (File f : file.listFiles()) {
                if (f.isFile()) {
                    f.delete();
                } else if (f.isDirectory()) {
                    deleteFile(f.getAbsolutePath());
                }
            }
        }

        return file.delete();
    }

    public static boolean deleteFileSafely(String path) {
        if (StringUtils.isBlank(path)) {
            return true;
        }
        File file = new File(path);
        if (!file.exists()) {
            return true;
        }
        if (file.isFile()) {
            deleteFileSafely(file);
            return true;
        }
        if (!file.isDirectory()) {
            return false;
        }
        if (file.listFiles() != null) {
            for (File f : file.listFiles()) {
                if (f.isFile()) {
                    deleteFileSafely(f);
                } else if (f.isDirectory()) {
                    deleteFileSafely(f.getAbsolutePath());
                }
            }
        }
        return deleteFileSafely(file);
    }

    /**
     * 安全删除文件.
     *
     * @param file
     * @return
     */
    public static boolean deleteFileSafely(File file) {
        if (file != null) {
            String tmpPath = file.getParent() + File.separator + System.currentTimeMillis();
            File tmp = new File(tmpPath);
            file.renameTo(tmp);
            return tmp.delete();
        }
        return false;
    }

    /**
     * get file size
     * <p>
     * <ul>
     * <li>if path is null or empty, return -1
     * <li>if path exist and it is a file, return file size, else return -1
     * <ul>
     *
     * @return returns the length of this file in bytes. returns -1 if the file does not exist.
     */
    public static long getFileSize(String path) {
        if (StringUtils.isBlank(path)) {
            return -1;
        }

        File file = new File(path);
        return (file.exists() && file.isFile() ? file.length() : -1);
    }

    public static boolean WriteFileToSD(String content, String path) {
        if (StringUtils.isEmpty(content) || StringUtils.isEmpty(path)) {

            return false;
        }

        FileOutputStream fos = null;
        try {
            fos = new FileOutputStream(new File(path));
            OutputStreamWriter osw = new OutputStreamWriter(fos, "UTF-8");
            BufferedWriter bw = new BufferedWriter(osw);
            bw.write(content + "\t\n");
            bw.close();
            osw.close();
            fos.close();
            return true;
        } catch (Exception e) {
            e.printStackTrace();
        } finally {

            if (fos != null) {
                try {
                    fos.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }

        return false;
    }

